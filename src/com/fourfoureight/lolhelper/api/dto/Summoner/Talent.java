package lolapi.dto.Summoner;

public class Talent {
	private int id; 			//	Talent id.
	private String name;		//	Talent name.
	private int rank; 			//	Talent rank.
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @return the rank
	 */
	public int getRank() {
		return rank;
	}
}

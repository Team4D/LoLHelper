package lolapi.dto.Summoner;

public class RuneSlot {
	private Rune rune;	//	Rune associated with the rune slot.
	private int runeSlotId;	//	Rune slot ID.
	/**
	 * @return the rune
	 */
	public Rune getRune() {
		return rune;
	}
	/**
	 * @return the runeSlotId
	 */
	public int getRuneSlotId() {
		return runeSlotId;
	}
}
